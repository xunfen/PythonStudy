#逻辑与：俩边都为True才为True
print(True and True)
print(True and False)
print(False and True)
print(False and False)

print("=======================")

#逻辑或：只要有一边为True，就返回True
print(True or True)
print(True or False)
print(False or True)
print(False or False)

print("=======================")

#逻辑非：True变False，False变True
print(not True)
print(not False)

print("=======================")
num = 15
print(num > 10 and num < 20)
print(num > 10 or num < 20)
